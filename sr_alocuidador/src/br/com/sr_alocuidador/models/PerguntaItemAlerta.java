public class PerguntaItemAlerta {

    private int codigo;
    private PerguntaItem perguntaitem;
    private String nome;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public PerguntaItem getPerguntaitem() {
        return perguntaitem;
    }

    public void setPerguntaitem(PerguntaItem perguntaitem) {
        this.perguntaitem = perguntaitem;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    
        
}
