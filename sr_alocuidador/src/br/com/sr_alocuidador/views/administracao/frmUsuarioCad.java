    private void Pesquisar() {
        try {
            Usuario usuario = daoUsuario.buscarPorId(xcodigo);
            if (usuario != null) {
                txtNome.setText(usuario.getNome());
                txtEmail.setText(usuario.getEmail());
                txtLogin.setText(usuario.getLogin());
                cbxAlerta.setSelected("S".equals(usuario.getAlerta()));
                cbxPesqSatisfacao.setSelected(usuario.getPesqsatisfacao() == 1);
                cbxProfMedico.setSelected("S".equals(usuario.getProfmedico()));
                cbxLace.setSelected("S".equals(usuario.getLace()));
                cbxAdministrador.setSelected("S".equals(usuario.getAdministrador()));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro de sql: " + ex.toString());
        }
    }

    private boolean validaDados() {
        if ("".equals(txtNome.getText())) {
            JOptionPane.showMessageDialog(null, "Nome não informado");
            txtNome.grabFocus();
            return false;
        } else if ("".equals(txtEmail.getText())) {
            JOptionPane.showMessageDialog(null, "Email não informado");
            txtEmail.grabFocus();
            return false;
        } else if ("".equals(txtLogin.getText())) {
            JOptionPane.showMessageDialog(null, "Login não informado");
            txtLogin.grabFocus();
            return false;
        } else {
            return true;
        }
    }

    private void gravar() {
        Usuario usuario = new Usuario();
        usuario.setCodigo(xcodigo);
        usuario.setNome(txtNome.getText());
        usuario.setEmail(txtEmail.getText());
        usuario.setLogin(txtLogin.getText());
        usuario.setAlerta(cbxAlerta.isSelected() ? "S" : "N");
        usuario.setPesqsatisfacao(cbxPesqSatisfacao.isSelected() ? 1 : 0);
        usuario.setProfmedico(cbxProfMedico.isSelected() ? "S" : "N");
        usuario.setLace(cbxLace.isSelected() ? "S" : "N");        
        usuario.setAdministrador(cbxAdministrador.isSelected() ? "S" : "N"); 
        try {
            daoUsuario.validaDados(usuario);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro de sql: " + ex.toString());
        }
    }
    

