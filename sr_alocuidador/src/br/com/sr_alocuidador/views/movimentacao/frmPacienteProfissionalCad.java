    private void Pesquisar() throws SQLException {
        PacienteProfissional p = daoPacienteProfissional.buscarPorId(xcodigo);
        if (p != null){
            txtNome.setText(p.getDescricao());
            txtEspecialidade.setText(p.getEspecialidade());
            txtRegistro.setText(p.getRegistro());
            txtTelefoneFixo.setText(p.getContato());
            txtTelefoneCelular.setText(p.getCelular());
            txtEmail.setText(p.getEmail());
        }
    }

    private boolean validaDados() {
        if ("".equals(txtNome.getText())){
            JOptionPane.showMessageDialog(null, "Nome não informado.");
            return false;
        } else if ("".equals(txtEspecialidade.getText())){
            JOptionPane.showMessageDialog(null, "Especialidade não informada");
            return false;
        } else if ("".equals(txtRegistro.getText())){
            JOptionPane.showMessageDialog(null, "Registro não informado");
            return false;
        } else if ("".equals(txtTelefoneFixo.getText())){
            JOptionPane.showMessageDialog(null, "Telefone Fixo não informado");
            return false;
        } else if ("".equals(txtTelefoneCelular.getText())){
            JOptionPane.showMessageDialog(null, "Telefone Celular não informado");
            return false;
        } else if ("".equals(txtEmail.getText())){
            JOptionPane.showMessageDialog(null, "Email não informado");
            return false;
        } else if ("".equals(txtRegistro.getText())){
            JOptionPane.showMessageDialog(null, "Registro não informado");
            return false;
        } else return true;        
        
    }

    private void gravar() throws SQLException {
        PacienteProfissional p = new PacienteProfissional();
        p.setCodigo(xcodigo);
        p.setPaciente(daoPaciente.buscarPorId(xcodpaciente));
        p.setDescricao(txtNome.getText());
        p.setEspecialidade(txtEspecialidade.getText());
        p.setContato(txtTelefoneFixo.getText());
        p.setCelular(txtTelefoneCelular.getText());
        p.setEmail(txtEmail.getText());
        p.setRegistro(txtRegistro.getText());
        daoPacienteProfissional.validaDados(p);
    }
